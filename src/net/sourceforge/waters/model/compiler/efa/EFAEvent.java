package net.sourceforge.waters.model.compiler.efa;

import net.sourceforge.waters.model.compiler.constraint.ConstraintList;
import net.sourceforge.waters.model.module.EventDeclProxy;
import net.sourceforge.waters.model.module.IdentifierProxy;
import net.sourceforge.waters.model.module.ModuleProxyCloner;
import net.sourceforge.waters.model.module.ModuleProxyFactory;
import net.sourceforge.waters.model.module.QualifiedIdentifierProxy;
import net.sourceforge.waters.plain.module.ModuleElementFactory;


/**
 * A compiler-internal representation of an atomic event in a system of
 * synchronised EFA. This class represents the actual events generated by
 * the compiler. Each EFA event has an associated event declaration ({@link
 * EFAEventDecl}) and a normalised guard expression ({@link
 * ConstraintList}) representing the possible values of the EFA variables
 * when this event is taken.
 *
 * @see EFACompiler
 * @author Robi Malik
 */

class EFAEvent {

  //#########################################################################
  //# Constructor
  EFAEvent(final EFAEventDecl edecl, final EFAVariableTransitionRelation rel)
  {
    mEFAEventDecl = edecl;
    mTransitionRelation = rel;
  }


  //#########################################################################
  //# Simple Access
  EFAEventDecl getEFAEventDecl()
  {
    return mEFAEventDecl;
  }

  EFAVariableTransitionRelation getTransitionRelation()
  {
    return mTransitionRelation;
  }

  boolean isBlocked()
  {
    return mTransitionRelation.isEmpty();
  }

  IdentifierProxy createIdentifier(final ModuleProxyFactory factory)
  {
    final EventDeclProxy decl = mEFAEventDecl.getEventDecl();
    final IdentifierProxy ident = decl.getIdentifier();
    return appendSuffix(factory, ident);
  }

  void setSuffix(final String suffix)
  {
    mSuffix = suffix;
  }


  //#########################################################################
  //# Auxiliary Methods
  private IdentifierProxy appendSuffix(final ModuleProxyFactory factory,
                                       final IdentifierProxy ident)
  {
    final ModuleProxyCloner cloner = factory.getCloner();
    if (ident instanceof QualifiedIdentifierProxy) {
      final QualifiedIdentifierProxy qual = (QualifiedIdentifierProxy) ident;
      final IdentifierProxy base = qual.getBaseIdentifier();
      final IdentifierProxy comp = qual.getComponentIdentifier();
      final IdentifierProxy baseclone =
        (IdentifierProxy) cloner.getClone(base);
      final IdentifierProxy compclone = appendSuffix(factory, comp);
      return factory.createQualifiedIdentifierProxy(baseclone, compclone);
    } else if (mSuffix.length() > 0) {
      final IdentifierProxy base = (IdentifierProxy) cloner.getClone(ident);
      final IdentifierProxy comp =
        factory.createSimpleIdentifierProxy(mSuffix);
      return factory.createQualifiedIdentifierProxy(base, comp);
    } else {
      return (IdentifierProxy) cloner.getClone(ident);
    }
  }


  //#########################################################################
  //# Debugging
  @Override
  public String toString()
  {
    if (mSuffix == null) {
      final EventDeclProxy decl = mEFAEventDecl.getEventDecl();
      return decl.getName() + ".*";
    } else {
      final ModuleProxyFactory factory = ModuleElementFactory.getInstance();
      final IdentifierProxy ident = createIdentifier(factory);
      return ident.toString();
    }
  }


  //#########################################################################
  //# Data Members
  private final EFAEventDecl mEFAEventDecl;
  private final EFAVariableTransitionRelation mTransitionRelation;
  private String mSuffix;

}
